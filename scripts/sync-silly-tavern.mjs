import { mkdir, writeFile } from 'node:fs/promises';
import { createWriteStream } from 'node:fs';
import { pipeline } from 'node:stream/promises';
import { fileURLToPath } from 'node:url';
import { dirname, resolve, join } from 'node:path';

const REPO_OWNER = 'test157t';
const REPO_NAME = 'VRM-Assets-Pack-For-Silly-Tavern';
const REPO_REF = 'main';
const ANIMATION_PATH = 'animation';
const API_URL = `https://api.github.com/repos/${REPO_OWNER}/${REPO_NAME}/contents/${ANIMATION_PATH}?ref=${REPO_REF}`;
const PUBLIC_DIR = resolve(dirname(fileURLToPath(import.meta.url)), '..', 'public', 'assets', 'animations', 'silly-tavern');
const MANIFEST_PATH = resolve(dirname(fileURLToPath(import.meta.url)), '..', 'src', 'lib', 'vrm', 'silly-tavern-manifest.ts');

async function fetchJson(url) {
  const response = await fetch(url, {
    headers: {
      'User-Agent': 'yourwifey-stream-sync',
    },
  });

  if (!response.ok) {
    throw new Error(`GitHub API request failed: ${response.status} ${response.statusText}`);
  }

  return response.json();
}

function slugifyFile(file) {
  return file
    .toLowerCase()
    .replace(/\.[^.]+$/, '')
    .replace(/[^a-z0-9]+/gi, '-')
    .replace(/(^-|-$)/g, '');
}

function shouldDownloadFile(entry) {
  if (entry.type !== 'file') {
    return false;
  }

  return /\.(bvh)$/i.test(entry.name) &&
    !['Readme.txt', 'desktop.ini'].includes(entry.name.toLowerCase());
}

async function downloadFile(downloadUrl, outputPath) {
  const response = await fetch(downloadUrl);
  if (!response.ok || !response.body) {
    throw new Error(`Download failed (${response.status}): ${downloadUrl}`);
  }

  await mkdir(dirname(outputPath), { recursive: true });
  const file = createWriteStream(outputPath);
  await pipeline(response.body, file);
}

async function main() {
  const entries = await fetchJson(API_URL);
  const filtered = entries.filter(shouldDownloadFile);

  if (filtered.length === 0) {
    throw new Error('No animation files found in the remote animation folder.');
  }

  const files = filtered
    .map((entry) => ({
      name: entry.name,
      downloadUrl: entry.download_url ?? '',
      slug: slugifyFile(entry.name),
    }))
    .sort((a, b) => a.name.localeCompare(b.name));

  await mkdir(PUBLIC_DIR, { recursive: true });

  // Download each remote clip for local playback during builds.
  for (const item of files) {
    const output = join(PUBLIC_DIR, item.name);
    if (item.downloadUrl) {
      console.log(`downloading ${item.name}`);
      await downloadFile(item.downloadUrl, output);
    }
  }

  const manifest = `// Auto-generated by scripts/sync-silly-tavern.mjs\n// Sourced from https://github.com/${REPO_OWNER}/${REPO_NAME}/tree/${REPO_REF}/${ANIMATION_PATH}\nexport const SILLY_TAVERN_ANIMATION_FILES = ${JSON.stringify(
    files.map((file) => file.name),
    null,
    2,
  )} as const;\n`;

  await writeFile(MANIFEST_PATH, manifest, 'utf8');
  console.log(`Synced ${files.length} animation files.`);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
